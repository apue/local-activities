#!/usr/bin/env node

import { fileURLToPath } from "node:url";

import { loadEnvFile, mergeEnvs } from "./env-inventory.mjs";

const payloadVersion = "2026-05-collector-v1";
const defaultCapabilities = [
  "wechat_browser",
  "dom_text",
  "image_capture",
  "vision_extraction",
];

export function buildFixtureRun({
  collectorId,
  runId,
  seedUrl,
  fixture,
  now = new Date(),
}) {
  const observedAt = now.toISOString();
  const startedAt = new Date(now.getTime() - 60_000).toISOString();
  const sourceRun = envelope({
    collectorId,
    runId,
    observedAt,
    payload: {
      seedUrl,
      status: fixture === "failure" ? "failed" : "success",
      startedAt,
      finishedAt: observedAt,
      checkedUrlCount: 1,
      articleCount: fixture === "failure" ? 0 : 1,
      draftCount: fixture === "failure" ? 0 : 1,
      failureCount: fixture === "failure" ? 1 : 0,
      failureReason: fixture === "failure" ? "parser_mismatch" : undefined,
      diagnostics: [
        {
          key: "fixture",
          value: `collector ${fixture} fixture`,
        },
      ],
    },
  });

  if (fixture === "failure") {
    return {
      sourceRun,
      collectorFailure: envelope({
        collectorId,
        runId,
        observedAt,
        payload: {
          articleUrl: seedUrl,
          stage: "draft_extraction",
          reason: "parser_mismatch",
          message: "Fixture failure uploaded by local collector smoke command.",
          retryable: false,
          diagnostics: [
            {
              key: "fixture",
              value: "structured failure fixture",
            },
          ],
        },
      }),
    };
  }

  return {
    sourceRun,
    articleSnapshot: envelope({
      collectorId,
      runId,
      observedAt,
      payload: {
        canonicalUrl: seedUrl,
        finalUrl: seedUrl,
        title: "Fixture Cultural Activity",
        authorName: "Fixture Collector",
        capturedAt: observedAt,
        languageHints: ["en", "zh"],
        captureMode: "text_complete",
        visibleText:
          "Fixture Cultural Activity by an official cultural organization in Beijing.",
        textHash: `${runId}-text`,
        evidenceAssetIds: [],
        contentHash: `${runId}-article`,
      },
    }),
    eventDraft: envelope({
      collectorId,
      runId,
      observedAt,
      payload: {
        articleUrl: seedUrl,
        extractionAttemptId: `${runId}-attempt`,
        captureMode: "text_complete",
        title: "Fixture Cultural Activity",
        originalTitle: "Fixture Cultural Activity",
        organizer: "Fixture Cultural Organization",
        startsAt: "2026-06-06T06:00:00.000Z",
        endsAt: "2026-06-06T08:00:00.000Z",
        timezone: "Asia/Shanghai",
        venueName: "Fixture Cultural Center",
        venueAddress: "Beijing",
        city: "Beijing",
        reservationStatus: "required",
        registrationAction: "Open reservation",
        registrationUrl: seedUrl,
        summary:
          "Deterministic collector fixture for verifying admin review ingestion.",
        entryNotes: "Generated by collector fixture smoke command.",
        signals: ["ready_for_review"],
        evidenceAssetIds: [],
        fieldEvidence: {
          title: ["visibleText"],
          startsAt: ["visibleText"],
          venueName: ["visibleText"],
        },
        confidence: 0.92,
      },
    }),
  };
}

export function createCollectorHeaders({
  collectorId,
  collectorApiKey,
  collectorJobId,
}) {
  const headers = {
    authorization: `Bearer ${collectorApiKey}`,
    "content-type": "application/json",
    "x-collector-id": collectorId,
  };
  if (collectorJobId) headers["x-collector-job-id"] = collectorJobId;
  return headers;
}

export async function runCollectorFixture({
  env = process.env,
  fetchImpl = fetch,
  now = new Date(),
  seedUrl,
  claimOnce = false,
  expectedJobId,
  fixture = "ready-event",
  runId = createRunId(now),
}) {
  const config = readCollectorConfig(env);
  let job;
  let effectiveSeedUrl = seedUrl;

  if (claimOnce) {
    const claimResponse = await postJson({
      baseUrl: config.baseUrl,
      path: "/api/collector/jobs/claim",
      headers: config.headers,
      fetchImpl,
      body: {
        collectorId: config.collectorId,
        capabilities: defaultCapabilities,
        maxJobs: 1,
      },
    });

    job = claimResponse.job;
    if (!job) {
      return {
        kind: "no-job",
        retryAfterSeconds: claimResponse.retryAfterSeconds,
      };
    }

    if (expectedJobId && job.jobId !== expectedJobId) {
      throw new Error(`claimed_unexpected_job:${job.jobId}`);
    }

    effectiveSeedUrl = job.seedUrl;
    await postJson({
      baseUrl: config.baseUrl,
      path: `/api/collector/jobs/${job.jobId}/heartbeat`,
      headers: config.headers,
      fetchImpl,
      body: {
        collectorId: config.collectorId,
        localRunId: runId,
        stage: "uploading",
        message: "Uploading collector fixture result",
        extendLeaseSeconds: 300,
      },
    });
  }

  if (!effectiveSeedUrl) {
    throw new Error("missing_seed_url");
  }

  const fixtureRun = buildFixtureRun({
    collectorId: config.collectorId,
    runId,
    seedUrl: effectiveSeedUrl,
    fixture,
    now,
  });

  const sourceRun = await postJson({
    baseUrl: config.baseUrl,
    path: "/api/collector/source-run",
    headers: config.headers,
    fetchImpl,
    body: fixtureRun.sourceRun,
  });

  const uploadedIds = {
    sourceRunId: sourceRun.id,
  };

  if (fixtureRun.articleSnapshot) {
    const articleSnapshot = await postJson({
      baseUrl: config.baseUrl,
      path: "/api/collector/article-snapshot",
      headers: config.headers,
      fetchImpl,
      body: fixtureRun.articleSnapshot,
    });
    uploadedIds.articleSnapshotId = articleSnapshot.id;
  }

  if (fixtureRun.eventDraft) {
    const eventDraft = await postJson({
      baseUrl: config.baseUrl,
      path: "/api/collector/event-draft",
      headers: config.headers,
      fetchImpl,
      body: fixtureRun.eventDraft,
    });
    uploadedIds.eventDraftId = eventDraft.id;
  }

  if (fixtureRun.collectorFailure) {
    const failure = await postJson({
      baseUrl: config.baseUrl,
      path: "/api/collector/failure",
      headers: config.headers,
      fetchImpl,
      body: fixtureRun.collectorFailure,
    });
    uploadedIds.failureId = failure.id;
  }

  if (job) {
    await postJson({
      baseUrl: config.baseUrl,
      path: `/api/collector/jobs/${job.jobId}/report`,
      headers: config.headers,
      fetchImpl,
      body: buildJobReport({
        collectorId: config.collectorId,
        runId,
        fixture,
        uploadedIds,
      }),
    });
  }

  return {
    kind: "uploaded",
    runId,
    jobId: job?.jobId,
    uploadedIds,
  };
}

export function formatFixtureSummary(result) {
  if (result.kind === "no-job") {
    return `No collector job available. retryAfterSeconds=${result.retryAfterSeconds ?? "unknown"}`;
  }

  const ids = Object.entries(result.uploadedIds)
    .map(([key, value]) => `${key}=${value}`)
    .join(" ");

  const job = result.jobId ? ` jobId=${result.jobId}` : "";

  return `Uploaded collector fixture runId=${result.runId}${job} ${ids}`;
}

function buildJobReport({ collectorId, runId, fixture, uploadedIds }) {
  if (fixture === "failure") {
    return {
      collectorId,
      localRunId: runId,
      status: "failed",
      sourceRunId: uploadedIds.sourceRunId,
      failureIds: uploadedIds.failureId ? [uploadedIds.failureId] : [],
      suggestedDisposition: "failed",
      message: "Collector fixture failure uploaded.",
    };
  }

  return {
    collectorId,
    localRunId: runId,
    status: "completed",
    sourceRunId: uploadedIds.sourceRunId,
    articleSnapshotIds: uploadedIds.articleSnapshotId
      ? [uploadedIds.articleSnapshotId]
      : [],
    eventDraftIds: uploadedIds.eventDraftId ? [uploadedIds.eventDraftId] : [],
    suggestedDisposition: "ready_for_review",
    message: "Collector fixture draft uploaded.",
  };
}

function readCollectorConfig(env) {
  const baseUrl = normalizeBaseUrl(
    env.COLLECTOR_BASE_URL ?? env.APP_BASE_URL ?? "",
  );
  const collectorId = env.COLLECTOR_ID?.trim();
  const collectorApiKey = env.COLLECTOR_API_KEY?.trim();

  if (!baseUrl) throw new Error("missing_collector_base_url");
  if (!collectorId) throw new Error("missing_collector_id");
  if (!collectorApiKey) throw new Error("missing_collector_api_key");

  return {
    baseUrl,
    collectorId,
    headers: createCollectorHeaders({ collectorId, collectorApiKey }),
  };
}

async function postJson({ baseUrl, path, headers, fetchImpl, body }) {
  const response = await fetchImpl(`${baseUrl}${path}`, {
    method: "POST",
    headers,
    body: JSON.stringify(body),
  });
  const data = await response.json().catch(() => ({}));

  if (!response.ok) {
    throw new Error(
      `collector_request_failed:${path}:${response.status}:${data.error ?? "unknown"}`,
    );
  }

  return data;
}

function envelope({ collectorId, runId, observedAt, payload }) {
  return {
    collectorId,
    runId,
    observedAt,
    payloadVersion,
    payload: removeUndefined(payload),
  };
}

function removeUndefined(value) {
  return Object.fromEntries(
    Object.entries(value).filter(([, entryValue]) => entryValue !== undefined),
  );
}

function normalizeBaseUrl(value) {
  return value.trim().replace(/\/+$/, "");
}

function createRunId(now) {
  return `fixture-${now.toISOString().replace(/[^0-9]/g, "").slice(0, 14)}`;
}

function parseArgs(argv) {
  const args = {
    envFile: undefined,
    seedUrl: undefined,
    claimOnce: false,
    fixture: "ready-event",
    runId: undefined,
    help: false,
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--help" || arg === "-h") {
      args.help = true;
    } else if (arg === "--env-file") {
      args.envFile = argv[index + 1];
      index += 1;
    } else if (arg === "--seed-url") {
      args.seedUrl = argv[index + 1];
      index += 1;
    } else if (arg === "--claim-once") {
      args.claimOnce = true;
    } else if (arg === "--fixture") {
      args.fixture = argv[index + 1];
      index += 1;
    } else if (arg === "--run-id") {
      args.runId = argv[index + 1];
      index += 1;
    } else {
      throw new Error(`unknown_arg:${arg}`);
    }
  }

  if (!["ready-event", "failure"].includes(args.fixture)) {
    throw new Error(`unknown_fixture:${args.fixture}`);
  }

  return args;
}

function printHelp() {
  console.log(`Usage: pnpm collector:fixture [--env-file .env] [--seed-url URL | --claim-once] [--fixture ready-event|failure]

Uploads a deterministic collector fixture through the authenticated Vercel collector APIs.

Required environment:
  COLLECTOR_BASE_URL or APP_BASE_URL
  COLLECTOR_API_KEY
  COLLECTOR_ID

Options:
  --env-file     Optional dotenv file merged over the current process env.
  --seed-url     Manual seed URL mode.
  --claim-once   Claim one queued Vercel collector job, upload fixture, then report.
  --fixture      Fixture kind. Defaults to ready-event.
  --run-id       Optional deterministic local run id.
  --help         Show this help text.`);
}

export async function runCli(argv = process.argv.slice(2), baseEnv = process.env) {
  const args = parseArgs(argv);
  if (args.help) {
    printHelp();
    return 0;
  }

  if (!args.seedUrl && !args.claimOnce) {
    throw new Error("missing_seed_url_or_claim_once");
  }

  const env = mergeEnvs(baseEnv, loadEnvFile(args.envFile));
  const result = await runCollectorFixture({
    env,
    seedUrl: args.seedUrl,
    claimOnce: args.claimOnce,
    fixture: args.fixture,
    runId: args.runId,
  });

  console.log(formatFixtureSummary(result));
  return 0;
}

const isMain = process.argv[1] === fileURLToPath(import.meta.url);
if (isMain) {
  try {
    process.exitCode = await runCli();
  } catch (error) {
    console.error(error instanceof Error ? error.message : String(error));
    process.exitCode = 1;
  }
}
